def add(x: float, y: float) -> float:
    return x + y

def sub(x: float, y: float) -> float:
    return x - y

def mult(x: float, y: float) -> float:
    return x*y

def div(x: float, y: float) -> float:
    return x/y

def idiv(x: int, y: int) -> int:
    return x//y

def mod(x: int, y: int) -> int:
    return x % y
